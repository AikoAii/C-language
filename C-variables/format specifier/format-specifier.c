#include <stdio.h>

int main() {
    int myAge = 17;
    float myHeight = 150.5;
    char myLetter = 'A';
    printf("%d\n",myAge); //output 17
    printf("%f\n",myHeight); //output 150.5
    printf("%c\n",myLetter); //output A

    //use format specifier %.. surrounded by double quotes (""), inside the printf() function
    /*
    use format specifier %.. surrounded by double quotes (""), inside the printf() function
    %d for int
    %c for char
    %f for float
    */

    return 0;
}