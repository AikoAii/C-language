#include <stdio.h>

int main() {
    int myArray[] = {1, 2, 3, 4, 5}; //To create an array, define the data type (like int) and specify the name of the array followed by square brackets [].

    //access array
    printf("array: %d\n", myArray[0]); //1
    printf("array: %d\n", myArray[2]); //3
    printf("array: %d\n", myArray[4]); //5

    return 0;
}