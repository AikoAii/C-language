#include <stdio.h>

int main() {
    //create variables
    int numInt = 5; //integer (whole number)
    float numFloat = 3.14; //floating point number
    char alpha = 'A'; //character

    //print variables
    printf("%d\n", numInt);
    printf("%f\n", numFloat);
    printf("%c", alpha);

    return 0;
}